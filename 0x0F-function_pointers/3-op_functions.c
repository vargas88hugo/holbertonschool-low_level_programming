#include "3-calc.h"
/**
 * op_add - Function that return a sum
 * @a: First number
 * @b: Second number
 * Return: The sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Function that return a differences
 * @a: First number
 * @b: Second number
 * Return: The differences between two numbers
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Function that return a product
 * @a: First number
 * @b: Second number
 * Return: The product between two numbers
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Function that return a division
 * @a: First number
 * @b: Second number
 * Return: The division between two numbers
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Function that return a remainder
 * @a: First number
 * @b: Seconde number
 * Return: The remainder between two numbers
 */
int op_mod(int a, int b)
{
	return (a % b);
}
